/*****************************************************************************
此程序用来在获取RSA密钥时，在已知n和e的情况下求d
******************************************************************************/

#include<stdio.h>

void main()
{
	long long tmp,x,y;
	long long r,e;
	r = 25600;
	e = 1409;
	tmp = x = y = 0;

	for(y = 1;;y++)
	{
		tmp = (1 + r * y)%e;
		if(tmp == 0)
		{
			x = (1 + r * y)/e;
			printf("tmp=%lld x=%lld y=%lld \n",tmp,x,y);
			return;
		}
	}

}
